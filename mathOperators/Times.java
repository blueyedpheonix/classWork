package mathOperators;

public class Times {
	
	public int kefel(int num1,int num2)
	{
		return num1 * num2;
	}
	
	public int plus(int num1,int num2)
	{
		return num1 + num2;
	}
	public int minus(int num1,int num2)
	{
		return num1 - num2;
	}
	public int hiluk(int num1,int num2)
	{
		return num1/num2;
	}

}
